def display_rainbow (colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])

  red = ["R"]
  orange = ["O"]
  yellow = ["Y"]
  green = ["G"]
  blue = ["B"]
  indigo = ["I"]
  violet = ["V"]

  puts "0: #{colors[red]}"
  puts "1: #{colors[orange]}"
  puts "2: #{colors[yellow]}"
  puts "3: #{colors[green]}"
  puts "4: #{colors[blue]}"
  puts "5: #{colors[indigo]}"
  puts "6: #{colors[violet]}"

end
