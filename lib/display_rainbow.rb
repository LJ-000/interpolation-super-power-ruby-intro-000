def display_rainbow (colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])

  red = ["R"]
  orange = ["O"]
  yellow = ["Y"]
  green = ["G"]
  blue = ["B"]
  indigo = ["I"]
  violet = ["V"]

  puts "R: #{colors[red]}"
  puts "O: #{colors[orange]}"
  puts "Y: #{colors[yellow]}"
  puts "G: #{colors[green]}"
  puts "B: #{colors[blue]}"
  puts "I: #{colors[indigo]}"
  puts "V: #{colors[violet]}"

end
